function ingresar_verde(){

    var texto1 = document.getElementById('entrada_verde').value;

    switch(texto1){
        case 'Green': swal({
            title: "Good job!",
            text: "That's the color!",
            icon: "success",
            button: "Aww yiss!",
          });
    }

    
    if(texto1  != 'Green'){
        swal({
            title: "Nice try!",
            text: "the color is Green",
            icon: "error",
            button: "Try again!",
          });
    }

}

